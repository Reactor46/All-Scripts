namespace Internal.Exchange.Migration
{
    using System;
    using System.IO;
    using System.Text;
    using System.Management.Automation.Host;

    public class MBXInfo
    {
        public String DN;
        public bool isActive;
        public ulong size;
        public DateTime? LastLogonTime;
        public DateTime? DisconnectDate;
        public String DisconnectReason;
    }

    public class MailboxStatisticsInfo
    {
        public static void ProcessItems(object[] items, bool getAll, String resultFilePath, PSHost host)
        {
            if (items == null)
            {
                host.UI.WriteLine("no items, bailing out.");
                return;
            }

            MBXInfo[] results = new MBXInfo[items.Length];
            int index = 0;

            foreach (object item in items)
            {
                Microsoft.Exchange.Data.Mapi.MailboxStatistics obj = (Microsoft.Exchange.Data.Mapi.MailboxStatistics)item;

                results[index] = new MBXInfo();
                results[index].DN = obj.MailboxGuid.ToString();
                results[index].LastLogonTime = obj.LastLogonTime;
                results[index].isActive = false;
                results[index].size = obj.TotalItemSize.Value.ToBytes() + obj.TotalDeletedItemSize.Value.ToBytes();
                if (getAll)
                {
                    results[index].DisconnectDate = obj.DisconnectDate;
                    if (obj.DisconnectReason != null)
                    {
                        results[index].DisconnectReason = obj.DisconnectReason.ToString();
                    }
                    else
                    {
                        results[index].DisconnectReason = String.Empty;
                    }
                }
                DateTime now = DateTime.Now;

                if (obj.LastLogonTime != null)
                {
                    TimeSpan history = (TimeSpan)(obj.LastLogonTime - now);
                    if (history.TotalDays < 30)
                    {
                        results[index].isActive = true;
                    }
                }
                
                index++;
            }

             StreamWriter resultFileStream = null;
            try
            {
               
                resultFileStream = new StreamWriter(resultFilePath, false, Encoding.ASCII);
                if (getAll)
                {
                    resultFileStream.WriteLine("DN, size, DisconnectDate, DisconnectReason");
                    foreach (MBXInfo result in results)
                    {
                        String line = result.DN + ", " + Convert.ToString(result.size) + ", " + Convert.ToString(result.DisconnectDate) + "," + result.DisconnectReason;
                        resultFileStream.WriteLine(line);
                    }
                }
                else
                {
                    resultFileStream.WriteLine("DN, isActive, size, LastLogonTime");
                    foreach (MBXInfo result in results)
                    {
                        String line = result.DN + ", " + Convert.ToString(result.isActive) + ", " + Convert.ToString(result.size) + "," + Convert.ToString(result.LastLogonTime);
                        resultFileStream.WriteLine(line);
                    }
                }
            }
            catch (Exception e)
            {
                host.UI.WriteLine("Got exception when writing results." + e.ToString());
            }
            finally
            {
                if (resultFileStream != null)
                {
                    resultFileStream.Close();
                }
            }
        }
    }
}

"@

