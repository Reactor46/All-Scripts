	{
        $stats = get-mailboxstatistics -database $input
        [Internal.Exchange.Migration.MailboxStatisticsInfo]::ProcessItems([Microsoft.Exchange.Data.Mapi.MailboxStatistics[]]$stats, $true, $resultFile, $Host)        
	}
	else
	{
        $stats = get-mailboxstatistics -database $input -noadlookup | ?{$_.DisconnectDate -eq $null}
        [Internal.Exchange.Migration.MailboxStatisticsInfo]::ProcessItems([Microsoft.Exchange.Data.Mapi.MailboxStatistics[]]$stats, $false, $resultFile, $Host)        
	}
}

